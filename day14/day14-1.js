const produceResult = () => {

    const lines = inputDoc.split('\n');
    let totalLoad = 0
    for (let column = 0; column < lines[0].length; column++) {
        const columnString = lines.map(line => line[column])
        let lineCounter = lines.length;
        let dotCount = 0
        // loop over all column elements
        for (const rock of columnString) {
            if (rock == "O") {totalLoad += lineCounter + dotCount};
            if (rock == ".") {dotCount++}
            if (rock == "#") {dotCount=0}
            lineCounter--
        }
    }



    console.log(`Total Load ${totalLoad}`)

}


